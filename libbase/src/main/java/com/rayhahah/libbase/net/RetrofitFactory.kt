class RetrofitFactory private constructor() {

    val retrofitMap: HashMap<String, Retrofit> = HashMap()
    val clietnMap: HashMap<String, OkHttpClient> = HashMap()
    fun get(baseUrl: String = ProjectConst.HOST): Retrofit {
        return retrofitMap[baseUrl]!!
    }

    fun getClient(baseUrl: String = ProjectConst.HOST): OkHttpClient {
        return clietnMap[baseUrl]!!
    }

    fun create(baseUrl: String, okHttpClient: OkHttpClient) {
        for (retrofitEntry in retrofitMap.entries) {
            if (retrofitEntry.key == baseUrl) {
                return
            }
        }

        clietnMap.put(baseUrl, okHttpClient)
        val initRetrofit = initRetrofit(baseUrl, okHttpClient)
        retrofitMap.put(baseUrl, initRetrofit)
    }

    private fun initRetrofit(baseUrl: String, okHttpClient: OkHttpClient): Retrofit {
        val retrofit = Retrofit.Builder()
            .baseUrl(baseUrl)
            .client(okHttpClient)
            .addConverterFactory(GsonConverterFactory.create())
//            .addConverterFactory(MoshiConverterFactory.create())
//            .addCallAdapterFactory(RxJava2CallAdapterFactory.create())
            .build()
        return retrofit
    }

    companion object {
        fun getInstance() = Holder.instance
    }

    object Holder {
        val instance = RetrofitFactory()
    }
}