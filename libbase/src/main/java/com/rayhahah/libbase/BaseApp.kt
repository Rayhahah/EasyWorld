open class BaseApp : Application() {

    override fun attachBaseContext(base: Context?) {
        super.attachBaseContext(base)
        mAppContext = this
    }

    override fun onCreate() {
        super.onCreate()
        ProjectConst.DEBUG = !AppHelper.isProductionMode(mAppContext)
        initLog()
    }

    private fun initLog() {
        LogUtils.getConfig()
            .setConsoleFilter(ProjectConst.TAG_TYPE)
            .setGlobalTag(ProjectConst.DEFAULT_TAG)
            .setLogSwitch(ProjectConst.DEBUG)
            .setBorderSwitch(false)
            .setConsoleSwitch(ProjectConst.DEBUG)
            .setLog2FileSwitch(false)
            .setStackDeep(1)
            .setDir(ProjectConst.Dir.LOG_DIR)
    }

    companion object {
        lateinit var mAppContext: BaseApp
        @JvmStatic
        fun getAppContext(): BaseApp {
            return mAppContext
        }
    }
}